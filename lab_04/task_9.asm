.MODEL SMALL
.STACK 100H
.DATA

NOTT DB "NOT DIVISIBLE BY 5 AND 11 $"
YES   DB "DIVISIBLE $"

.CODE
MAIN PROC
; Initialize DS
MOV AX, @DATA
MOV DS, AX


MOV AX, 55     


MOV BX, 5
DIV BX         

CMP AH, 0       
JNE CHECK_11    


CHECK_11:
MOV BX, 11
DIV BX      

CMP AH, 0       
JNE PRINT_NOT_DIV


LEA DX, YES
MOV AH, 9
INT 21H
JMP EXIT

PRINT_NOT_DIV:
LEA DX, NOTT
MOV AH, 9
INT 21H

EXIT:
; Exit to DOS
MOV AX, 4C00H
INT 21H
MAIN ENDP
END MAIN
