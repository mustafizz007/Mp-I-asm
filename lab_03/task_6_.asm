
.MODEL SMALL
.STACK 100H
.DATA 


HEX DB "Enter a Hex Digit: $"
DEK DB "In Decimal it is: $"

.CODE
MAIN PROC
;iniitialize DS
MOV AX,@DATA
MOV DS,AX
;enter your code here


MOV AH, 9    ;HEX
LEA DX, HEX
INT 21H 

MOV AH, 1      ;INPUT
INT 21H

SUB AL, 'A'    ;CONVERSION HEX TO DEC
ADD AL, 10


MOV BL, AL


MOV AH , 2      ;NEW LINE
MOV DL, 0DH
INT 21H
MOV DL, 0AH
INT 21H 

MOV AH, 9    ;DEC
LEA DX, DEK
INT 21H 

MOV AH, 2
MOV DL, BL
ADD DL, '0'
INT 21H 








;exit to DOS
MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN